package com.raulh82vlc.cleanrequests.ui.fragments;

import android.app.Fragment;
import android.content.Context;
import android.net.ConnectivityManager;
import android.net.NetworkInfo;
import android.os.Bundle;
import android.view.View;

import butterknife.ButterKnife;

/**
 * Logic for all fragments, injects and destroys view injections
 * <p/>
 * Created by Raul Hernandez Lopez on 05/09/2015.
 */
public abstract class BaseFragment extends Fragment {

    @Override
    public void onViewCreated(View view, Bundle savedInstanceState) {
        super.onViewCreated(view, savedInstanceState);
        ButterKnife.inject(this, view);
    }

    @Override
    public void onDestroyView() {
        super.onDestroyView();
        ButterKnife.reset(this);
    }

    /**
     * isThereInternetConnection checks
     * the internect connection and returns
     * true or false
     *
     * @param context the actual context
     * @return isConnected boolean with the result
     * <p/>
     * Created by Raul Hernandez Lopez on 05/09/2015.
     */
    protected boolean isThereInternetConnection(Context context) {
        boolean isConnected;
        ConnectivityManager connectivityManager =
                (ConnectivityManager) context.getSystemService(Context.CONNECTIVITY_SERVICE);
        NetworkInfo networkInfo = connectivityManager.getActiveNetworkInfo();
        isConnected = (networkInfo != null && networkInfo.isConnectedOrConnecting());
        return isConnected;
    }
}
